#include <stdio.h>
void ft_print_comb2(void)
{
	int i, j;
	for(i = 0; i <= 9; i++)
	{
		for(j = 0 + 1; j <= 9; j++)
		{
			printf("%d%d ", i, j);
		}
	}
}
int main()
{
	ft_print_comb2();
	return 0;
	}
